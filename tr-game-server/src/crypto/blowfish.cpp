void CBlowfish::BFInit() 
{
	BLOWFISH_CTX* ctx = &this->BlowfishContext;
	for (int i=0; i < 4; i++) 
	{
		for (int j=0; j < 256; j++)
		{
			ctx->S[i][j] = this->ORIG_S[i][j];
		}
	}
	memcpy((void*)ctx->P, this->BF_PTransformed, sizeof(BF_PTransformed));//18*4);
	memcpy((void*)ctx->S, (void*)this->sdata, sizeof(sdata));//0x100*4*4);
}

void CBlowfish::BFEncrypt(uint32_t *xl, uint32_t *xr)
{
	BLOWFISH_CTX* ctx = &this->BlowfishContext;
	uint32_t  Xl;
	uint32_t  Xr;
	uint32_t  temp;
	
	Xl = *xl;
	Xr = *xr;
	for (uint16_t i=0; i < N; ++i) 
	{
		Xl = Xl ^ ctx->P[i];
	    Xr = F(Xl) ^ Xr;
		
	    temp = Xl;
	    Xl = Xr;
	    Xr = temp;
	}
	temp = Xl;
	Xl = Xr;
	Xr = temp;
	Xr = Xr ^ ctx->P[N];
	Xl = Xl ^ ctx->P[N + 1];
	*xl = Xl;
	*xr = Xr;
}

void CBlowfish::BFDecrypt(uint32_t *xl, uint32_t *xr)
{
	BLOWFISH_CTX* ctx = &this->BlowfishContext;
	uint32_t  Xl;
	uint32_t  Xr;
	uint32_t  temp;
	
	Xl = *xl;
	Xr = *xr;
	for (uint16_t i=N+1; i > 1; --i) 
	{
		Xl = Xl ^ ctx->P[i];
		Xr = F(Xl) ^ Xr;
		
		//Exchange Xl and Xr
		temp = Xl;
		Xl = Xr;
		Xr = temp;
	}
	//Exchange Xl and Xr
	temp = Xl;
	Xl = Xr;
	Xr = temp;
	
	Xr = Xr ^ ctx->P[1];
	Xl = Xl ^ ctx->P[0];
	
	*xl = Xl;
	*xr = Xr;
}

uint32_t CBlowfish::F(uint32_t x) 
{
	BLOWFISH_CTX* ctx = &this->BlowfishContext;
	uint16_t a, b, c, d;
	uint32_t  y;
	
	d = (uint16_t)(x & 0xFF);
	x >>= 8;
	c = (uint16_t)(x & 0xFF);
	x >>= 8;
	b = (uint16_t)(x & 0xFF);
	x >>= 8;
	a = (uint16_t)(x & 0xFF);
	y = ctx->S[0][a] + ctx->S[1][b];
	y = y ^ ctx->S[2][c];
	y = y + ctx->S[3][d];
	
	return y;
}

