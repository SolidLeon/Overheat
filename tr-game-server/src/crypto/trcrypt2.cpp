//Diffie Hellman
void CTRCrypt2::setup_dh()
{
    int codes = 0;
    dh = DH_new();
    dh = DH_generate_parameters(0x40*8,5,0,0);
    BN_bin2bn(DH_Constant_Prime, 0x40, dh->p);
    DH_check(dh, &codes);
    DH_generate_key(dh);
}

