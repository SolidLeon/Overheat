void CTRCrypt2::DH_UpdateB(BIGNUM* newB)
{
    BN_copy(bnB, newB);
    BN_mod_exp(bnK,        bnB,               dh->priv_key,        dh->p,              bn_ctx);
    //Init Tabula_Crypt2
    uint8_t K[0x40] = {0};
    BN_bn2bin(bnK, K);
    Tabula_CryptInit2(&tbc2, K);
} 
